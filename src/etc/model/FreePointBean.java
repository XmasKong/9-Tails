package etc.model;

public class FreePointBean
{	
	private int freepoint_num;
	private String freepoint_get_day;
	private int freepoint_recharge;
	private int freepoint_use;
	private String freepoint_end_day;
	private int member_num;
	
	public int getFreepoint_num() {
		return freepoint_num;
	}
	public void setFreepoint_num(int freepoint_num) {
		this.freepoint_num = freepoint_num;
	}
	public String getFreepoint_get_day() {
		return freepoint_get_day;
	}
	public void setFreepoint_get_day(String freepoint_get_day) {
		this.freepoint_get_day = freepoint_get_day;
	}
	public int getFreepoint_recharge() {
		return freepoint_recharge;
	}
	public void setFreepoint_recharge(int freepoint_recharge) {
		this.freepoint_recharge = freepoint_recharge;
	}
	public int getFreepoint_use() {
		return freepoint_use;
	}
	public void setFreepoint_use(int freepoint_use) {
		this.freepoint_use = freepoint_use;
	}
	public String getFreepoint_end_day() {
		return freepoint_end_day;
	}
	public void setFreepoint_end_day(String freepoint_end_day) {
		this.freepoint_end_day = freepoint_end_day;
	}
	public int getMember_num() {
		return member_num;
	}
	public void setMember_num(int member_num) {
		this.member_num = member_num;
	}
}
