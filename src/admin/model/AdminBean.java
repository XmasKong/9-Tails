package admin.model;

public class AdminBean {
	private int admin_num;
	private String admin_id;
	private String admin_pwd;
	private String admin_name;
	private String admin_email;
	private int admin_grade_num;
	private String admin_grade_name;
	
	public int getAdmin_num() {
		return admin_num;
	}
	public void setAdmin_num(int admin_num) {
		this.admin_num = admin_num;
	}
	public String getAdmin_id() {
		return admin_id;
	}
	public void setAdmin_id(String admin_id) {
		this.admin_id = admin_id;
	}
	public String getAdmin_pwd() {
		return admin_pwd;
	}
	public void setAdmin_pwd(String admin_pwd) {
		this.admin_pwd = admin_pwd;
	}
	public String getAdmin_name() {
		return admin_name;
	}
	public void setAdmin_name(String admin_name) {
		this.admin_name = admin_name;
	}
	public String getAdmin_email() {
		return admin_email;
	}
	public void setAdmin_email(String admin_email) {
		this.admin_email = admin_email;
	}
	public int getAdmin_grade_num() {
		return admin_grade_num;
	}
	public void setAdmin_grade_num(int admin_grade_num) {
		this.admin_grade_num = admin_grade_num;
	}
	public String getAdmin_grade_name() {
		return admin_grade_name;
	}
	public void setAdmin_grade_name(String admin_grade_name) {
		this.admin_grade_name = admin_grade_name;
	}
}
